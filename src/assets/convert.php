function restructure_json($data) {
    // Рекурсивна функція для обробки об'єктів та масивів
    if (is_array($data) || is_object($data)) {
        $new_data = (is_object($data)) ? (object)[] : [];
        foreach ($data as $key => $value) {
            if (is_array($value) || is_object($value)) {
                if (isset($value->coord)) {
                    $parsed = explode(';',$value->coord) ;
                    $new_data->{$key} = (object) [
                        'location' => (object) [
                            'lat' => $parsed[0],
                            'lng' => $parsed[1]
                        ]
                    ];
                    // Копіюємо інші ключі
                    foreach ($value as $sub_key => $sub_value) {
                        if ($sub_key !== 'coord' && $sub_key !== 'zip') {
                            $new_data->{$key}->{$sub_key} = restructure_json($sub_value);
                        }
                    }
                } else {
                    if ($key !== 'coord' && $key !== 'zip') {
                        $new_data->{$key} = restructure_json($value);
                    }
                }
            } else {
                if ($key !== 'coord' || $key !== 'zip') {
                    $new_data->{$key} = $value;
                }

            }
        }
        return $new_data;
    }
    return $data;
}

